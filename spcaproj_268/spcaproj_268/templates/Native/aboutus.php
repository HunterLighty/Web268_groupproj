			<p>The Adams County Society for the Prevention of Cruelty to Animals (SPCA), is an IRC 501 c-3 non-profit organization that provides shelter and care for lost, abandoned and abused animals, investigates cases of cruelty to animals, recommends the prosecution of persons engaging in animal neglect or cruelty, helps control animal overpopulation and educates the community about spay/neuter programs, animal wellness and safety.

The Adams County SPCA is a private nonprofit organization that receives less than 1% of its annual operating budget of $632,300 from Adams County. We also receive donations from several townships/municipalities for our much needed services, but we continue to struggle to support the very important task of caring for the many animals that come through our doors. We do fundraising, memberships, apply for grants and receive some bequests. We take in approximately 2200 animals per year and each year that number increases.
​
The Adams County SPCA is supported by eight Full-Time employees (including one Veterinarian), six Part-Time employees – along with an active Board of Directors, and many committed volunteers who help support the work of the shelter. </p>
		
